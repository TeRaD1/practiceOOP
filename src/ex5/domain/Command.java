package ex5.domain;

/**
 * Інтерфейс для реалізації патерну "Команда" (Command).
 * Описує базові операції: виконання команди та її скасування (undo).
 */
public interface Command {
    /**
     * Виконує команду.
     */
    void execute();

    /**
     * Відміняє (скасовує) виконання команди.
     */
    void undo();
}
